package Biblioteca;

public class DVD {
    private String nome, região, distribuidora, duração, sinopse, bônus, ano, gênero, UPC;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRegião() {
        return região;
    }

    public void setRegião(String região) {
        this.região = região;
    }

    public String getDistribuidora() {
        return distribuidora;
    }

    public void setDistribuidora (String distribuidora) {
        this. distribuidora = distribuidora;
    }
    
    public String getDuração(String duração) {
        return duração;
    }

    public void setDuração(String duração) {
        this.duração = duração;
    }

    public String getSinopse() {
        return sinopse;
    }

    public void setSinopse(String sinopse) {
        this.sinopse = sinopse;
    }

    public String getBônus() {
        return bônus;
    }

    public void setBônus(String bônus) {
        this.bônus = bônus;
    }

    public String getAno() {
        return ano;
    }

    public void setAno(String ano) {
        this.ano = ano;
    }

    public String getGênero() {
        return gênero;
    }

    public void setGênero(String gênero) {
        this.gênero = gênero;
    }

    public String getUPC() {
        return UPC;
    }

    public void setUPC(String UPC) {
        this.UPC = UPC;
    }
}
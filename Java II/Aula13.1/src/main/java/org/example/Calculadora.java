package org.example;

public class Calculadora {
    public double soma(double a, double b) {
        return a + b;
    }

    public double subtracao(double a, double b) {
        return a - b;
    }

    public double divisao(double a, double b) {
        return a / b;
    }


    public double multi(double a, double b) {
        return a * b;
    }
}